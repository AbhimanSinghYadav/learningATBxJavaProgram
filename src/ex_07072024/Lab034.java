package ex_07072024;

public class Lab034 {
    public static void main(String[] args) {
        //Relational operator--boolean
        // <,> >=,<=,==,!=,(!=)- true/false
        int a=6;
        int b=3;
        boolean c= a<b;
        boolean d=a>b;
        boolean e=a>=b;//OR gate
        boolean f=a==b;
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(!f);
        System.out.println(!(10 >30));

    }
}
